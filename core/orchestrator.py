def report_generator_node(state: ComplianceState) -> Dict[str, Any]:
    """
    Full report generator — uses Gemini 2.5 Flash to synthesise a professional
    Markdown compliance audit report from all accumulated alerts.
    Falls back to a clean template if no API key is available.
    """
    print("--- AGENT 4: REPORT GENERATOR ---")
    alerts = state.get("alerts", [])
    rules  = state.get("active_rules", [])
    escalation = state.get("escalation_status", "NONE")
    flagged    = state.get("flagged_entities", [])
    run_ts     = datetime.now().strftime("%Y-%m-%d %H:%M:%S UTC")
    human_feedback = state.get("human_feedback", "")

    if human_feedback and human_feedback.startswith("REJECTED"):
        reject_reason = human_feedback.replace("REJECTED:", "").strip()
        report = (
            "# 🛑 Investigation Rejected by Compliance Officer\n\n"
            f"**Generated**: {run_ts}\n\n"
            "This investigation was manually reviewed and dismissed.\n\n"
            f"**Rejection Reason**: {reject_reason}\n\n"
            "All underlying alerts have been archived and this entity pattern has been added to the ChromaDB False Positives feedback loop. No further action is required."
        )
        print("INFO [ReportGenerator]: Alerts were rejected by human. Bypassing LLM synthesis.")
        return {"report_content": report}

    # ── Fallback template (no LLM) ───────────────────────────────────────────
    def _build_fallback_report() -> str:
        lines = [
            "# Compliance Monitoring Audit Report",
            f"**Generated**: {run_ts}",
            f"**Escalation Status**: {escalation}",
            f"**Total Alerts**: {len(alerts)}",
            f"**Flagged Entities**: {', '.join(flagged) if flagged else 'None'}",
            "",
            "---",
            "",
            "## Active Regulatory Rules Retrieved",
        ]
        if rules:
            for r in rules:
                lines.append(f"- **{r.get('jurisdiction','?')}**: {r.get('description','')[:120]}...")
        else:
            lines.append("_No rules retrieved from ChromaDB (vector store not yet ingested)._")
        lines += ["", "---", "", "## Compliance Alerts"]
        if alerts:
            for i, a in enumerate(alerts, 1):
                lines.append(
                    f"### Alert {i}: [{a.get('risk_level','N/A')}] {a.get('source_agent','?')}\n"
                    f"- **Entity**: `{a.get('entity_id','?')}` ({a.get('entity_type','?')})\n"
                    f"- **Reason**: {a.get('reason','?')}\n"
                    f"- **Timestamp**: {a.get('timestamp','?')}"
                )
        else:
            lines.append("_No compliance violations detected._")
        lines += ["", "---", "", "## Verdict",
                  f"Overall escalation status: **{escalation}**. "
                  "Review flagged entities and escalate to Compliance Officer if required."]
        return "\n".join(lines)

    # ── LLM synthesis ────────────────────────────────────────────────────────
    report: str
    if not _GOOGLE_KEY_PRESENT or not alerts:
        report = _build_fallback_report()
        if not alerts:
            print("INFO [ReportGenerator]: No alerts — using clean-bill template.")
    else:
        try:
            llm = ChatGoogleGenerativeAI(
                model="gemini-3.1-flash-lite",
                api_key=os.getenv("GOOGLE_API_KEY"),
                temperature=0.2,
                max_output_tokens=2048,
            )
            prompt = ChatPromptTemplate.from_messages([
                ("system",
                 "You are a Senior Compliance Officer writing a formal audit report for a Tier-2 Bank. "
                 "You will be given: (1) regulatory rules from a vector store, "
                 "(2) compliance alerts from automated agents, which may include special META-ALERTs "
                 "from the Correlation Engine (source_agent = 'Correlation Engine (META)').\\n\\n"
                 "META-ALERTs indicate that the SAME entity was flagged by BOTH the Transaction Monitor "
                 "AND the Communication Scanner — this is a multi-channel coordinated breach pattern "
                 "and must be treated with the HIGHEST urgency.\\n\\n"
                 "Write a professional Markdown audit report with these sections:\\n"
                 "1. Executive Summary (lead with any coordinated breach findings)\\n"
                 "2. Regulatory Framework (cite each rule by its jurisdiction/rule ID)\\n"
                 "3. Coordinated Breach Analysis — ONLY if META-ALERTs exist: one sub-section per "
                 "   META-ALERT, showing the linked_alert_ids evidence chain and why the co-occurrence "
                 "   elevates the risk beyond the individual alerts\\n"
                 "4. Individual Findings (one sub-section per unique entity_id for non-META alerts)\\n"
                 "5. Risk Matrix (table: Entity | Violation Type | Source | Risk Level | Recommended Action)\\n"
                 "6. Recommended Actions (prioritise CRITICAL and CORRELATED_ENTITY entities first)\\n"
                 "7. Conclusion\\n"
                 "Be precise — cite alert IDs, rule IDs, and the full entity ID trail for each finding."),
                ("user",
                 "Run timestamp: {run_ts}\\n"
                 "Escalation status: {escalation}\\n"
                 "Flagged entities: {flagged}\\n\\n"
                 "=== REGULATORY RULES RETRIEVED FROM CHROMADB ===\\n{rules}\\n\\n"
                 "=== ALL COMPLIANCE ALERTS (including META-ALERTs from Correlation Engine) ===\\n{alerts}"),
            ])
            chain = prompt | llm
            response = chain.invoke({
                "run_ts":     run_ts,
                "escalation": escalation,
                "flagged":    ", ".join(flagged) if flagged else "None",
                "rules":      rules,
                "alerts":     alerts,
            })
            if isinstance(response.content, list):
                report = "".join(
                    chunk["text"] if isinstance(chunk, dict) and "text" in chunk
                    else str(chunk)
                    for chunk in response.content
                )
            else:
                report = response.content
            print("INFO [ReportGenerator]: LLM synthesis complete.")
        except Exception as e:
            print(f"ERROR [ReportGenerator]: LLM synthesis failed ({e}). Using fallback.")
            report = _build_fallback_report()

    print(report[:500] + "...\n[truncated]" if len(report) > 500 else report)
    return {"report_content": report}


# ── Router ───────────────────────────────────────────────────────────────────

def _route_after_conflict_resolution(state: ComplianceState) -> str:
    """
    Routes to HITL for CRITICAL/HIGH alerts; direct to report for everything else.
    """
    status = state.get("escalation_status", "NONE")
    if status in ("ESCALATED", "PENDING_REVIEW"):
        return "hitl_placeholder"
    return "report_generator"


# ── Graph Builder ─────────────────────────────────────────────────────────────

def build_orchestrator(enable_hitl=True):
    """
    Compiles and returns the LangGraph application.
    Call once at startup and reuse the compiled app.
    """
    workflow = StateGraph(ComplianceState)

    # Register nodes
    workflow.add_node("regulatory_tracker",    get_regulatory_tracker_node())
    workflow.add_node("transaction_monitor",   get_transaction_monitor_node())
    workflow.add_node("communication_scanner", get_communication_scanner_node())
    workflow.add_node("conflict_resolution",   conflict_resolution_node)
    workflow.add_node("hitl_placeholder",      hitl_placeholder_node)
    workflow.add_node("report_generator",      report_generator_node)

    # Sequential edges
    workflow.set_entry_point("regulatory_tracker")
    workflow.add_edge("regulatory_tracker",    "transaction_monitor")
    workflow.add_edge("transaction_monitor",   "communication_scanner")
    workflow.add_edge("communication_scanner", "conflict_resolution")

    # Conditional edge: escalate or report directly
    workflow.add_conditional_edges(
        "conflict_resolution",
        _route_after_conflict_resolution,
        {
            "hitl_placeholder": "hitl_placeholder",
            "report_generator": "report_generator",
        },
    )

    # HITL always leads to report after human review
    workflow.add_edge("hitl_placeholder", "report_generator")
    workflow.add_edge("report_generator", END)

    if enable_hitl:
        # langgraph 0.2.x + langgraph-checkpoint-sqlite 1.0.x
        try:
            from langgraph.checkpoint.sqlite import SqliteSaver
        except ImportError:
            print("WARNING [Orchestrator]: SqliteSaver unavailable — falling back to MemorySaver.")
            return workflow.compile(
                checkpointer=MemorySaver(),
                interrupt_before=["report_generator"]
            )

        db_path = "checkpoints.sqlite"
        import sqlite3
        # Connect explicitly so the connection stays open for the lifetime of the app
        conn = sqlite3.connect(db_path, check_same_thread=False)
        memory = SqliteSaver(conn)
        if hasattr(memory, "setup"):
            memory.setup()

        return workflow.compile(
            checkpointer=memory,
            interrupt_before=["report_generator"]
        )
    else:
        return workflow.compile()

# Expose a globally compiled app instance for the Streamlit dashboard
app = build_orchestrator(enable_hitl=True)


