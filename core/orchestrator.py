# ── Correlation Engine (Day 9) ───────────────────────────────────────────────

_TX_AGENT_KEYWORDS   = {"Transaction", "transaction", "TRANSACTION"}
_COMM_AGENT_KEYWORDS = {"Communication", "communication", "COMMUNICATION", "Compliance AI"}


def _agent_type(source_agent: str) -> str:
    """Classify an alert by its source agent type for cross-reference matching."""
    if any(kw in source_agent for kw in _TX_AGENT_KEYWORDS):
        return "TRANSACTION"
    if any(kw in source_agent for kw in _COMM_AGENT_KEYWORDS):
        return "COMMUNICATION"
    return "OTHER"


def conflict_resolution_node(state: ComplianceState) -> Dict[str, Any]:
    """
    Day 9 Correlation Engine (Semantic Upgraded).
    """
    print("--- AGENT: CORRELATION ENGINE (Semantic Resolution) ---")
    alerts = state.get("alerts", [])
    
    if not alerts:
        return {"alerts": []}

    # Format alerts for the LLM prompt
    formatted_alerts = [
        {
            "alert_id": a.get("alert_id"),
            "source_agent": a.get("source_agent"),
            "entity_id": a.get("entity_id"),
            "risk_level": a.get("risk_level"),
            "reason": a.get("reason"),
            "entities_involved": a.get("entities_involved", []),
            "timestamp": a.get("timestamp")
        } for a in alerts
    ]
    
    new_meta_alerts = []

    if _GOOGLE_KEY_PRESENT:
        llm = ChatGoogleGenerativeAI(
            model="gemini-3.1-flash-lite",
            api_key=os.getenv("GOOGLE_API_KEY"),
            temperature=0,
        )
        analyzer_llm = llm.with_structured_output(AlertList)

        prompt = ChatPromptTemplate.from_messages([
            ("system", """You are the Lead Correlation Engine for a Tier-2 Bank.
Your job is to read all the compliance alerts generated by the Transaction Monitor and Communication Scanner.

Look for SEMANTIC LINKS across different alerts. A coordinated breach might involve different entities.
Example: 
- Alert A (Communication): SPOUSE_001 mentions "Buy TSLA now, John gave me the signal."
- Alert B (Transaction): TRADER_007 executes a massive TSLA buy 2 minutes later.

If you find semantic links (matching tickers, closely aligned timestamps, explicit mentions), 
generate a CRITICAL META-ALERT that fuses them together. 
- The 'alert_id' MUST start with 'META_'
- The 'entities_involved' should list ALL linked entities (e.g. SPOUSE_001, TRADER_007).
- The 'reason' must explicitly explain the semantic link you found.
- The 'linked_alert_ids' must contain the original alert IDs.

Do NOT generate new alerts for isolated events. ONLY generate META-ALERTs for correlated events.
"""),
            ("user", "Generated Alerts:\n{alerts}")
        ])

        chain = prompt | analyzer_llm

        try:
            print("INFO [CorrelationEngine]: Running semantic reasoning over alerts...")
            llm_output = chain.invoke({
                "alerts": formatted_alerts
            })
            
            if llm_output and hasattr(llm_output, 'alerts'):
                for meta_alert in llm_output.alerts:
                    meta_alert.alert_id = f"META_{uuid.uuid4().hex[:8].upper()}"
                    meta_alert.source_agent = "Correlation Engine (Semantic)"
                    meta_alert.risk_level = "CRITICAL"
                    meta_alert.entity_type = "CORRELATED_ENTITY"
                    new_meta_alerts.append(meta_alert.to_state_dict())
                    print(f"  [META-ALERT] {meta_alert.alert_id}: {meta_alert.reason[:80]}...")
                    
        except Exception as e:
            print(f"Error in Semantic Correlation: {e}")
    else:
        print("INFO [CorrelationEngine]: No API key present. Skipping semantic matching.")

    # Calculate overall escalation status based on ALL alerts
    all_alerts_combined = alerts + new_meta_alerts
    severity_rank = {"CRITICAL": 4, "HIGH": 3, "MEDIUM": 2, "LOW": 1}
    max_rank = max(
        (severity_rank.get(a.get("risk_level", "LOW").upper(), 0)
         for a in all_alerts_combined),
        default=0,
    )
    max_severity = {v: k for k, v in severity_rank.items()}.get(max_rank, "NONE")

    if max_rank >= 4:
        escalation = "ESCALATED"
    elif max_rank >= 3:
        escalation = "PENDING_REVIEW"
    else:
        escalation = "NONE"

    # Gather all flagged entities
    all_entity_ids = list({
        eid
        for a in all_alerts_combined
        for eid in ([a.get("entity_id", "")] + a.get("entities_involved", []) + a.get("linked_alert_ids", []))
        if eid and not eid.startswith("ALERT") and not eid.startswith("META")
    })

    print(
        f"INFO [CorrelationEngine]: {len(new_meta_alerts)} META-ALERT(s) generated. "
        f"Max severity: {max_severity}. Escalation: {escalation}."
    )

    return {
        "escalation_status": escalation,
        "flagged_entities":  all_entity_ids,
        "alerts":            new_meta_alerts,
    }


